namespace Cauldron.Cryptography
{
    /// <summary>
    /// Defines the RSA key sizes
    /// </summary>
    public enum RSAKeySizes : uint
    {
        /// <summary>
        /// The key size of 1024 bits
        /// </summary>
        Size1024 = 1024,

        /// <summary>
        /// The key size of 2048 bits
        /// </summary>
        Size2048 = 2048,

        /// <summary>
        /// The key size of 4096 bits
        /// </summary>
        Size4096 = 4096
    }
}